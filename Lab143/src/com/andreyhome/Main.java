package com.andreyhome;

public class Main {

    public static void main(String[] args) {
        boolean b1 = true;
        boolean b2 = false;

        System.out.println(b1 | b2); //применяем оператор or
        System.out.println(b1 & b2); //применяем оператор and
        System.out.println(b1 ^ b2); //применяем оператор xor
        System.out.println(!b1); //применяем оператор not

    }
}
